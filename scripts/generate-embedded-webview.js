const fs = require('fs');
const path = require('path');

const webviewDir = path.join(__dirname, '..', 'src', 'webview', 'web');
const outputFile = path.join(__dirname, '..', 'src', 'services', 'mobile', 'routes', 'embedded-webview.ts');

const files = [
  'index.html',
  'password.html',
  'manifest.json',
  'styles.css',
  'js/app.js',
  'js/constants.js',
  'js/core/MobileInterface.js',
  'js/explorer/FileExplorer.js',
  'js/git/GitChanges.js',
  'js/utils/ansi.js',
  'js/utils/html.js',
  'js/utils/syntax.js',
  'js/utils/time.js'
];

function escapeForTemplateLiteral(content) {
  return content
    .replace(/\\/g, '\\\\')
    .replace(/`/g, '\\`')
    .replace(/\${/g, '\\${');
}

let output = '// Auto-generated file - webview static files embedded as string constants\n';
output += '// This file is generated by scripts/generate-embedded-webview.js\n';
output += '// Do not edit manually - run the generator script instead\n\n';

for (const file of files) {
  const filePath = path.join(webviewDir, file);
  const content = fs.readFileSync(filePath, 'utf8');
  const escaped = escapeForTemplateLiteral(content);
  
  const constName = file
    .replace(/[^a-zA-Z0-9]/g, '_')
    .replace(/_+/g, '_')
    .toUpperCase();
  
  output += 'export const ' + constName + " = `" + escaped + "`;\n\n";
}

fs.writeFileSync(outputFile, output);
console.log('Generated embedded-webview.ts with', files.length, 'files');
